#!/usr/bin/env python3
"""
Summarization script for the research pipeline.
Generates summaries for downloaded content using Claude.
"""

import os
import sys
import subprocess
from datetime import datetime, date, timedelta
from pathlib import Path

# Add scripts dir to path for imports
sys.path.insert(0, str(Path(__file__).parent))
from utils import (
    load_config, log, setup_logging, extract_text,
    get_topics_dir, get_digests_dir, sanitize_filename
)


def get_unsummarized_files(topic: str, base_dir: Path) -> list:
    """Find source files that don't have corresponding summaries."""
    sources_dir = base_dir / "topics" / topic / "sources"
    notes_dir = base_dir / "topics" / topic / "notes"
    notes_dir.mkdir(parents=True, exist_ok=True)

    if not sources_dir.exists():
        return []

    unsummarized = []
    existing_summaries = {f.stem.replace("-summary", "") for f in notes_dir.glob("*-summary.md")}

    # Scan all date subdirectories
    for date_dir in sources_dir.iterdir():
        if not date_dir.is_dir():
            continue

        for source_file in date_dir.iterdir():
            if source_file.suffix.lower() in [".pdf", ".md", ".txt", ".html", ".htm"]:
                stem = sanitize_filename(source_file.stem)
                if stem not in existing_summaries:
                    unsummarized.append(source_file)

    return unsummarized


def summarize_with_claude(content: str, topic_config: dict, filename: str) -> str:
    """Generate a summary using Claude CLI."""
    summary_focus = topic_config.get("summary_focus", "key findings and practical implications")

    prompt = f"""Summarize this document for a research digest.

Focus on: {summary_focus}

Structure your summary as:
1. Key Findings (2-3 sentences)
2. Methodology (study design, sample, approach) - if applicable
3. Notable Data (specific statistics, effect sizes, numbers)
4. Relevance (why this matters, practical implications)
5. Follow-up (open questions, related work to explore)

Be specific about numbers. Don't hedge on findings the document states clearly.
If effect sizes are reported, include them. If methodology has limitations
the authors acknowledge, note them.

Document title: {filename}

---
CONTENT:
{content[:15000]}
"""

    try:
        # Use Claude CLI for summarization
        # Pipe prompt via stdin to avoid Windows command line length limits
        result = subprocess.run(
            ["claude", "--print"],
            input=prompt,
            capture_output=True,
            text=True,
            timeout=120,
            shell=True
        )

        if result.returncode == 0:
            return result.stdout.strip()
        else:
            log("summarize", "error", f"Claude CLI error: {result.stderr}")
            return None

    except subprocess.TimeoutExpired:
        log("summarize", "error", "Claude CLI timed out")
        return None
    except FileNotFoundError:
        log("summarize", "error", "Claude CLI not found. Install with: npm install -g @anthropic-ai/claude-code")
        return None
    except Exception as e:
        log("summarize", "error", f"Summarization failed: {e}")
        return None


def save_summary(summary: str, source_file: Path, topic: str, base_dir: Path) -> Path:
    """Save a summary to the notes directory."""
    notes_dir = base_dir / "topics" / topic / "notes"
    notes_dir.mkdir(parents=True, exist_ok=True)

    stem = sanitize_filename(source_file.stem)
    summary_file = notes_dir / f"{stem}-summary.md"

    content = f"""# Summary: {source_file.stem}

**Source:** `{source_file.relative_to(base_dir)}`
**Summarized:** {datetime.now().strftime('%Y-%m-%d %H:%M')}

---

{summary}

---
*Generated by research pipeline*
"""

    with open(summary_file, "w", encoding="utf-8") as f:
        f.write(content)

    return summary_file


def process_topic(topic: str, topic_config: dict, base_dir: Path) -> int:
    """Process unsummarized files for a topic. Returns count of summaries generated."""
    log("summarize", "info", f"Processing topic: {topic}")

    unsummarized = get_unsummarized_files(topic, base_dir)

    if not unsummarized:
        log("summarize", "info", f"No new files to summarize for {topic}")
        return 0

    log("summarize", "info", f"Found {len(unsummarized)} files to summarize")

    summaries_generated = 0

    for source_file in unsummarized:
        log("summarize", "info", f"Summarizing: {source_file.name}")

        # Extract text content
        content = extract_text(source_file)
        if not content or len(content.strip()) < 100:
            log("summarize", "warning", f"Insufficient content in {source_file.name}, skipping")
            continue

        # Generate summary
        summary = summarize_with_claude(content, topic_config, source_file.name)

        if summary:
            summary_file = save_summary(summary, source_file, topic, base_dir)
            log("summarize", "info", f"Summary saved: {summary_file.name}")
            summaries_generated += 1
        else:
            log("summarize", "warning", f"Failed to summarize {source_file.name}")

    return summaries_generated


def generate_weekly_rollup(config: dict, base_dir: Path):
    """Generate a cross-topic weekly rollup if it's the configured day."""
    rollup_day = config.get("settings", {}).get("rollup_day", "sunday").lower()
    today = date.today()

    # Check if today is the rollup day
    day_names = ["monday", "tuesday", "wednesday", "thursday", "friday", "saturday", "sunday"]
    if day_names[today.weekday()] != rollup_day:
        return

    log("summarize", "info", "Generating weekly rollup...")

    rollups_dir = get_digests_dir(base_dir) / "rollups"
    rollups_dir.mkdir(parents=True, exist_ok=True)

    rollup_file = rollups_dir / f"week-of-{today.isoformat()}.md"

    # Collect summaries from the past 7 days
    week_ago = today - timedelta(days=7)
    topics_data = {}

    for topic_name in config.get("topics", {}).keys():
        notes_dir = base_dir / "topics" / topic_name / "notes"
        if not notes_dir.exists():
            continue

        recent_summaries = []
        for summary_file in notes_dir.glob("*-summary.md"):
            # Check file modification time
            mtime = datetime.fromtimestamp(summary_file.stat().st_mtime)
            if mtime.date() >= week_ago:
                with open(summary_file, "r", encoding="utf-8") as f:
                    content = f.read()
                recent_summaries.append({
                    "file": summary_file.name,
                    "content": content
                })

        if recent_summaries:
            topics_data[topic_name] = recent_summaries

    if not topics_data:
        log("summarize", "info", "No summaries from past week, skipping rollup")
        return

    # Generate rollup content
    total_summaries = sum(len(s) for s in topics_data.values())

    content = f"""# Weekly Research Rollup — Week of {today.isoformat()}

## Overview
- **Topics tracked:** {len(config.get('topics', {}))}
- **Topics with activity this week:** {len(topics_data)}
- **Total summaries generated:** {total_summaries}

"""

    for topic_name, summaries in topics_data.items():
        topic_config = config["topics"].get(topic_name, {})
        frequency = topic_config.get("frequency", "weekly")

        content += f"""## {topic_name.replace('-', ' ').title()} ({frequency})

**{len(summaries)} items summarized this week**

"""
        for s in summaries:
            # Extract just the title from summary file name
            title = s["file"].replace("-summary.md", "").replace("-", " ").title()
            content += f"- {title}\n"

        content += "\n"

    content += f"""---

## Cross-Topic Patterns

*Review the individual summaries above and identify any themes that span multiple research topics.*

---
*Generated by research pipeline on {datetime.now().strftime('%Y-%m-%d %H:%M')}*
"""

    with open(rollup_file, "w", encoding="utf-8") as f:
        f.write(content)

    log("summarize", "info", f"Weekly rollup saved: {rollup_file}")


def main():
    """Main entry point for the summarization script."""
    # Determine base directory
    base_dir = Path.cwd()
    if not (base_dir / "config.yaml").exists():
        base_dir = Path(__file__).parent.parent
        if not (base_dir / "config.yaml").exists():
            print("Error: config.yaml not found. Run from research directory.")
            sys.exit(1)

    # Setup logging
    setup_logging(base_dir / "logs")
    log("summarize", "info", "=" * 50)
    log("summarize", "info", "Starting summarization run")

    # Load config
    try:
        config = load_config(base_dir / "config.yaml")
    except Exception as e:
        log("summarize", "error", f"Failed to load config: {e}")
        sys.exit(1)

    # Process each topic
    total_summaries = 0
    for topic_name, topic_config in config.get("topics", {}).items():
        count = process_topic(topic_name, topic_config, base_dir)
        total_summaries += count

    log("summarize", "info", f"Summarization complete. {total_summaries} summaries generated.")

    # Generate weekly rollup if appropriate
    generate_weekly_rollup(config, base_dir)


if __name__ == "__main__":
    main()
